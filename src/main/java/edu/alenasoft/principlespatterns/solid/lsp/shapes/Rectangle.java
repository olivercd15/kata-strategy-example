package edu.alenasoft.principlespatterns.solid.lsp.shapes;

public final class Rectangle implements Shape {

    private int width;
    private int height;

    public void setWidth(int width) {
        this.width = width;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    @Override
    public int getArea() {
        return width * height;
    }
}
